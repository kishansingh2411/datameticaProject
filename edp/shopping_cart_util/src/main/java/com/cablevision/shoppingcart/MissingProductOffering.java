package com.cablevision.shoppingcart;

public class MissingProductOffering {
	
	boolean isProductNull = false;
	
	boolean isOfferNull = false;

	public boolean isProductNull() {
		return isProductNull;
	}

	public void setProductNull(boolean isProductNull) {
		this.isProductNull = isProductNull;
	}

	public boolean isOfferNull() {
		return isOfferNull;
	}

	public void setOfferNull(boolean isOfferNull) {
		this.isOfferNull = isOfferNull;
	}

}
